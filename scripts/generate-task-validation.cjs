#!/usr/bin/env node
/**
 * Generates a task-specific agent-browser E2E validation script for the AI Task Runner.
 * Reads task title/description to infer which page to validate; defaults to login + home.
 *
 * Usage:
 *   node scripts/generate-task-validation.cjs --id <uuid> --title "..." --description "..." [--out-dir test-results/task-xxx]
 */

const fs = require('fs')
const path = require('path')

/** Placeholder used by ai-task-runner.sh when title/description are empty (bash drops empty args) */
const EMPTY_PLACEHOLDER = '__EMPTY__'

function parseArgs() {
  const args = process.argv.slice(2)
  const out = { id: '', title: '', description: '', outDir: 'test-results/task-validation' }
  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--id' && i + 1 < args.length) {
      out.id = args[++i]
    } else if (args[i] === '--title' && i + 1 < args.length) {
      const val = args[++i]
      out.title = val === EMPTY_PLACEHOLDER ? '' : val
    } else if (args[i] === '--description' && i + 1 < args.length) {
      const val = args[++i]
      out.description = val === EMPTY_PLACEHOLDER ? '' : val
    } else if (args[i] === '--out-dir' && i + 1 < args.length) {
      out.outDir = args[++i]
    }
  }
  return out
}

function inferTargetPath(title, description) {
  const hasContent = (title || '').trim() || (description || '').trim()
  if (!hasContent) return '/roadmap'
  const text = `${(title || '').toLowerCase()} ${(description || '').toLowerCase()}`
  if (/\broadmap\b|\bkanban\b|\bbacklog\b|\buser review\b/.test(text)) return '/roadmap'
  if (/\bfinancial\b|\bcashflow\b|\bkpi\b|\bbudget\b/.test(text)) return '/financial'
  if (/\barchitect\b|\btime.?track|\btimesheet\b|\bdashboard\b/.test(text)) return '/architect'
  if (/\bproject\b|\bschedule\b|\bwbs\b/.test(text)) return '/projects'
  if (/\bmaterial\b|\bprocurement\b/.test(text)) return '/materials-labor'
  if (/\bsettings\b|\bdisplay\s*settings\b/.test(text)) return '/roadmap' // roadmap has display settings
  return '/architect' // default: architect dashboard (post-login)
}

const { id, title, description, outDir } = parseArgs()
if (!id) {
  console.error('Missing --id')
  process.exit(1)
}

const idShort = id.slice(0, 8)
const targetPath = inferTargetPath(title, description)
const outDirAbs = path.isAbsolute(outDir) ? outDir : path.join(process.cwd(), outDir)
const scriptPath = path.join(process.cwd(), 'e2e', `task-validation-${idShort}.agent-browser.cjs`)

const script = `#!/usr/bin/env node
// Generated by scripts/generate-task-validation.cjs for task ${idShort}
// Title: ${(title || '').replace(/'/g, "\\'")}
// Validates: ${targetPath}

const fs = require('fs')
const { spawnSync } = require('child_process')

const baseUrl = process.env.BASE_URL || 'http://localhost:5173'
const session = 'e2e-task-${idShort}-' + Date.now()
const outDir = ${JSON.stringify(outDirAbs)}

const readEnvValueFromFile = (filePath, key) => {
  if (!fs.existsSync(filePath)) return undefined
  const line = fs
    .readFileSync(filePath, 'utf8')
    .split('\\n')
    .find((entry) => entry.startsWith(key + '='))
  if (!line) return undefined
  return line.replace(key + '=', '').replace(/^"|"$/g, '')
}

const readEnvValue = (key) =>
  readEnvValueFromFile('.env', key) || readEnvValueFromFile('.env.testing', key)

const email = process.env.ACCOUNT_TEST_EMAIL || readEnvValue('ACCOUNT_TEST_EMAIL')
const password = process.env.ACCOUNT_TEST_EMAIL_PASSWORD || readEnvValue('ACCOUNT_TEST_EMAIL_PASSWORD')

if (!email || !password) {
  console.error('Missing ACCOUNT_TEST_EMAIL or ACCOUNT_TEST_EMAIL_PASSWORD in .env.testing')
  process.exit(1)
}

fs.mkdirSync(outDir, { recursive: true })

const run = (args, { allowFail = false } = {}) => {
  const result = spawnSync('agent-browser', ['--session', session, ...args], {
    encoding: 'utf8',
    shell: false,
  })
  if (result.status !== 0) {
    if (allowFail) return false
    throw new Error(result.stderr ? result.stderr.trim() : 'agent-browser failed')
  }
  return true
}

const loginAndNavigate = () => {
  run(['open', baseUrl + '/login'])
  run(['fill', '#email', email])
  run(['fill', '#password', password])
  run(['click', 'button[type=submit]'])
  run(['wait', '3500'])
  run(['open', baseUrl + '${targetPath}'])
  run(['wait', '2500'])
  const stillOnLogin = run(['find', '#email'], { allowFail: true })
  if (stillOnLogin) {
    throw new Error('Login failed')
  }
}

try {
  loginAndNavigate()
  run(['screenshot', outDir + '/01-page.png', '--full'])
  run(['eval', "if (!document.body || document.body.innerText.length < 50) throw new Error('Page content too small')"])
  console.log('✅ Task validation passed: page loaded and content present')
  console.log('📸 Evidence: ' + outDir)
} catch (error) {
  console.error('❌ Task validation failed: ' + error.message)
  process.exit(1)
}
`

fs.mkdirSync(path.dirname(scriptPath), { recursive: true })
fs.writeFileSync(scriptPath, script, 'utf8')
fs.chmodSync(scriptPath, 0o755)
console.log('Wrote', scriptPath)
